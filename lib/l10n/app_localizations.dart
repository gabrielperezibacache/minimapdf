  /// Quita el sufijo localizado "(marcador)" de cualquier idioma soportado.
  static String stripSignedMarker(String title) {
    var result = title.trimRight();
    for (final marker in allSignedMarkers) {
      final suffix = ' ($marker)';
      if (result.endsWith(suffix)) {
        result = result.substring(0, result.length - suffix.length).trimRight();
      }
    }
    return result;
  }

